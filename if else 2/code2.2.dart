void main() {
  int x = 3;
  if (x == 1) {
    print("voilet");
  } else if (x == 2) {
    print("indigo");
  } else if (x == 3) {
    print("blue");
  } else if (x == 4) {
    print("green");
  } else if (x == 5) {
    print("yellow");
  } else if (x == 6) {
    print("orange");
  } else {
    print("invalid index");
  }
}
